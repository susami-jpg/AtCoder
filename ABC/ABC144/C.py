INF = 10**15
MOD = 10**9+7

N = int(input())
#約数列挙
def make_divisors(n):
    lower_divisors , upper_divisors = [], []
    i = 1
    while i*i <= n:
        if n % i == 0:
            lower_divisors.append(i)
            if i != n // i:
                upper_divisors.append(n//i)
        i += 1
    return lower_divisors + upper_divisors[::-1]

cnd = make_divisors(N)
ans = INF
for c in cnd:
    ans = min(ans, c + N//c - 2)
print(ans)
